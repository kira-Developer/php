<?php


/*

Increment / Decrement Operators

[ ++$var ] Pre Increment 
[ $var++ ] Post Increment
[ --$var ] Pre Decrement
[ $var-- ] Post Decrement 
*/

$var1 = 10;


   // Pre Increment

echo "<h1>Pre Increment </h1>"."<br>";

echo ++$var1 . "<br>";

echo ++$var1 . "<br>";

echo ++$var1 . "<br>";

echo ++$var1 . "<br>";



$var2 = 20;

echo "<h1>Post Increment </h1>"."<br>";

echo $var2++ . "<br>";

echo $var2++ . "<br>";

echo $var2++ . "<br>";

echo $var2++ . "<br>";




$var3 = 30;

echo "<h1> Pre Decrement</h1>"."<br>";

echo --$var3 . "<br>";


echo --$var3 . "<br>";


echo --$var3 . "<br>";


echo --$var3 . "<br>";

$var4 = 10;
echo "<h1>Post Decrement</h1>"."<br>";

echo $var4-- . "<br>";


echo $var4-- . "<br>";


echo $var4-- . "<br>";


echo $var4-- . "<br>";
